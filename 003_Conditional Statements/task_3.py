a = float(input("Введите первое число:"))
b = float(input("Введите второе число:"))
c = float(input("Введите третье число:"))
d = b**2 - 4 * a * c
if d < 0:
    print("Отсутствие действительных корней!")
elif d == 0:
    x1 = -(b / 2 * a)
    print("x1 =", x1)
elif d > 0:
    x1 = (-b + d**0.5) / (2 * a)
    x2 = (-b - d**0.5) / (2 * a)
    print("x1 =", x1, "x2 =", x2)
